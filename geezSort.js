// Create a lookup map for better performance
const amharicIndexMap = new Map();
amharicAlphabet.forEach((char, index) => {
  amharicIndexMap.set(char, index);
});

// Sorting function with error handling
function geezSort(arr) {
  if (!Array.isArray(arr)) {
    throw new Error("Input must be an array of strings.");
  }

  // Function to check if a word contains only Amharic characters
  function isAmharicWord(word) {
    return [...word].every((char) => amharicIndexMap.has(char));
  }

  return arr.sort((a, b) => {
    const isAAmharic = isAmharicWord(a);
    const isBAmharic = isAmharicWord(b);

    // Move non-Amharic words to the end
    if (isAAmharic && !isBAmharic) return -1;
    if (!isAAmharic && isBAmharic) return 1;
    if (!isAAmharic && !isBAmharic) return 0; // Maintain order among non-Amharic words

    // If both are Amharic, apply Amharic sorting logic
    for (let i = 0; i < Math.max(a.length, b.length); i++) {
      const charA = a[i] || ""; // Handle shorter words
      const charB = b[i] || "";

      const indexA = amharicIndexMap.has(charA) ? amharicIndexMap.get(charA) : -1;
      const indexB = amharicIndexMap.has(charB) ? amharicIndexMap.get(charB) : -1;

      if (indexA !== indexB) {
        return indexA - indexB;
      }
    }

    return 0; // If words are identical
  });
}

// Export module
module.exports = geezSort;
